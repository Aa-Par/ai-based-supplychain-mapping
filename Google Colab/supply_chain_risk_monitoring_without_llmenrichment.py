def run_agent(scenario: str):
    """
    Full pipeline:
      1. Run deterministic baseline scoring.
      2. Run ownership analysis.
      3. Render Plotly dashboard.
    """
    print("=" * 70)
    print(f"  🇮🇳 {AGENT_TITLE.upper()}")
    print(f"  Ownership Depth: ≥{OWNERSHIP_DEPTH_PCT}%")
