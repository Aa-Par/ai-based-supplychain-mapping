# ── CELL 7: Ownership Analysis Engine ────────────────────────
STATE_TYPES = {
    "Government", "Sovereign Wealth Fund", "Government Investment",
    "Government Pension Fund", "Government Insurer", "SOE", "Industrial Conglomerate",
    "Public Sector",
}

def run_ownership_analysis(suppliers: list, registry: dict, depth_pct: float = 1.0) -> list:
    results = []
    for s in suppliers:
        name = s.get("name", "")
        owners = registry.get(name, [])
        flagged_owners = [o for o in owners if o["stake_pct"] >= depth_pct]

        state_owners = [
            o for o in flagged_owners
            if o["type"] in STATE_TYPES or SOE_KEYWORDS.search(o["type"]) or SOE_KEYWORDS.search(o["owner"])
        ]
        total_state_pct = min(sum(o["stake_pct"] for o in state_owners), 100)
        soe_flag = (
            any(o["stake_pct"] > 25 and (o["type"] in STATE_TYPES or SOE_KEYWORDS.search(o["type"])) for o in flagged_owners)
            or total_state_pct > 40
        )
        results.append({
            **s,
            "owners":            flagged_owners,
            "state_owners":      state_owners,
            "total_state_pct":   round(total_state_pct, 1),
            "soe_flag":          soe_flag,
            "ownership_risk_score": min(20, int(total_state_pct / 5)),
        })
    return results


# ── CELL 8: Cost premium lookup ───────────────────────────────
# Countries are grouped by strategic proximity to India.
# This gives each alternate a realistic, varied cost premium
# instead of a flat 12% for every single alternate.

_ALLIED_COUNTRIES = {
    "India", "Russia", "Israel", "France", "United States", "USA",
    "UK", "United Kingdom", "Japan", "South Korea", "Australia",
}
_NEUTRAL_COUNTRIES = {
    "Germany", "Italy", "Sweden", "Spain", "Brazil", "Singapore",
    "Malaysia", "Turkey", "UAE", "South Africa",
}

def _cost_premium_pct(supplier_name: str, country: str) -> int:
    """
    Returns a varied cost-premium estimate based on the alternate
    supplier's country and a deterministic offset from the supplier name,
    so each row shows a different realistic value.
    """
    # Name-hash gives a small ±offset so identical-country alternates still differ
    name_offset = sum(ord(c) for c in supplier_name) % 7  # 0-6

    if country in _ALLIED_COUNTRIES:
        base = 8
    elif country in _NEUTRAL_COUNTRIES:
        base = 18
    else:
        base = 28   # non-allied / unknown geography → higher premium

    return base + name_offset   # e.g. 8-14% for India, 18-24% neutral, 28-34% other


