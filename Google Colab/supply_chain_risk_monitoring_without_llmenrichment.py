    analysis, _ = run_baseline_country_agent(crisis_country=scenario.split()[0])
    print(f"  ↳ {len(analysis['suppliers'])} suppliers scored | Risk level: {analysis['overall_risk_level']}")

    print(f"\n🏛️  Step 2/2 — Ownership analysis (≥{OWNERSHIP_DEPTH_PCT}%)...")
